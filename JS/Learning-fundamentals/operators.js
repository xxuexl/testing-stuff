/*-------------OPERADORES DE ASIGNACIÓN-------------------------------
son un tipo de operadores que se utilizan para asignar valores a variables. 
Son =, +=, -=, *= y /=. */

/*-------------OPERADORES LÓGICOS-------------------------------------
Se utilizan para evaluar expresiones lógicas y devolver un valor booleano
(true or false). Son &&(and), ||(or) !(not)
&& and : Evalúa 2 expresionse lógicas y devuelve True si ambas son true. 
|| or  : Evalúa 2 expresiones lógicas y devuelve True si al menos 1 de ellas es true.
! not  : Invierte el valor booleano de su operando. Si es true, es false. */

console.log(true && false);
console.log(true || false);
console.log(!false);
