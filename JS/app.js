//About const in arrays and objects

//Objects are keys and values pairs.
const dictionary = {
  name: "joe",
  likes: "cats",
};
dictionary.name = "chu";
console.log(dictionary);

//Arrays are ordered lists.
const people = [];

people[0] = "loki";
people[1] = "mochi";
console.log(people[0]);

const colours = ["blue", "green", "red"];
colours[1] = "pink";
console.log(colours[1]);
